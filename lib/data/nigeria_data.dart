import 'package:country_state_city/country_state_city.dart' as csc;

class NigeriaData {
  static const String countryCode = 'NG';

  static List<csc.State> getStates() {
    return _nigeriaLgas.keys.map((stateName) {
      return csc.State(
        name: stateName,
        isoCode:
            _stateIsoCodes[stateName] ??
            stateName.substring(0, 2).toUpperCase(), // Fallback ISO
        countryCode: countryCode,
      );
    }).toList();
  }

  /// Returns all LGAs for a given State ISO Code as [csc.City] objects.
  static List<csc.City> getLgas(String stateIsoCode) {
    // Find state name by ISO code
    final stateName = _stateIsoCodes.entries
        .firstWhere(
          (e) => e.value == stateIsoCode,
          orElse: () => const MapEntry('', ''),
        )
        .key;

    if (stateName.isEmpty || !_nigeriaLgas.containsKey(stateName)) {
      return [];
    }

    final lgas = _nigeriaLgas[stateName]!;
    return lgas.map((lgaName) {
      return csc.City(
        name: lgaName,
        countryCode: countryCode,
        stateCode: stateIsoCode,
      );
    }).toList();
  }

